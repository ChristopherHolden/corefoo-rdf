function expand(aliasedReference) {
    return aliasedReference.replace('wd:','https://www.wikidata.org/entity/');
}

var nationalLanguage = index.getResourceByUri('http://www.corefoo.org/ont/geo/nationalLanguage');
function relateLanguage(subjectRef, objectRef) {
    var subject = index.getResourceByUri(expand(subjectRef));
    var object = index.getResourceByUri(expand(objectRef));
    index.relate(subject, nationalLanguage, object);
}


